package a1115;

public class For1 {
    public static void main(String[] args) {
        for(int i = 1; i <= 10; i++){
            System.out.println(i + " ");
        }
        for(int i = 50; i <= 100; i++){
            System.out.println(i + " ");
        }
        
        for(int i = 2; i <= 20; i += 2){
            System.out.println(i + " ");
        }

        for(int i = 11; i <= 20; i += 2){
            System.out.println(i + " ");
        }
    }
}
